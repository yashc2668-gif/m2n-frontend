const permissionEquivalents = new Map<string, Set<string>>();

for (const group of permissionEquivalenceGroups) {
  const normalizedGroup = new Set(group.map((permission) => permission.trim().toLowerCase()));
  for (const permission of normalizedGroup) {
    permissionEquivalents.set(permission, normalizedGroup);
  }
}

