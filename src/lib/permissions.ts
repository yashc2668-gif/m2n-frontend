export function normalizeRole(role: string): AppRole {
  const normalized = role.trim().toLowerCase().replace(/\s+/g, '_').replace(/-/g, '_');
  if (normalized in roleDefinitions) {
    return normalized as AppRole;
  }
  return 'viewer';
}

export function getRoleLabel(role: string) {
  return roleDefinitions[normalizeRole(role)].label;
}

export function hasPermissions(role: string, permissions: string[]) {
  const normalizedRole = normalizeRole(role);
  const granted = rolePermissions[normalizedRole];

  if (granted.has('*')) {
    return true;
  }

  return permissions.every((permission) => {
    const normalizedPermission = permission.trim().toLowerCase();
    const aliases = permissionEquivalents.get(normalizedPermission) ?? new Set([normalizedPermission]);
    return Array.from(aliases).some((alias) => granted.has(alias));
  });
}

export function hasRole(role: string, allowedRoles: AppRole[]) {
  return allowedRoles.includes(normalizeRole(role));
}
